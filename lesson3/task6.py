def int_func(word):
    """
    Берет на вход слово, написанное маленькими латинскими буквами, возвращает слово
    с большой буквы
    :param word: str, слово маленькими буквами
    :return: str, слово с большой буквы
    """
    if not word.islower():
        word.islower()

    #try:
      # word.isalpha()
   # except ValueError:
       # return (f'Слово {word} содержит не латинские символы')

    return word.capitalize()


