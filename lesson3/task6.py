def int_func_2(s):
    """
    Функция берет на вход предложение из слов, написанных маленькими латинскими буквами и
    возвращает предложение, где все слова написаны с большой буквы. Используем ранее
    написанную функцию для слова
    :param s: str
    :return: str
    """
    return ' '.join(int_func(w) for w in s.split(' '))


print(int_func_2(input('Введите предложение из слов, написанных маленькими латинскими буквами: ')))
